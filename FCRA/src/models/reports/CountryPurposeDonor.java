package models.reports;

public class CountryPurposeDonor {
	private String assoRcn;
	private String assoName;
	private String district;
	private String state;
	private String amount;
	private String blockYear;
	private String country;
	private String donor;
	private String purpose;
	
	
	public CountryPurposeDonor(String blockYear,String country, String donor, String purpose,String assoRcn, String assoName,
			String state, String district, String amount
			) {		
		this.assoRcn = assoRcn;
		this.assoName = assoName;
		this.district = district;
		this.state = state;
		this.amount = amount;
		this.blockYear = blockYear;
		this.country = country;
		this.donor = donor;
		this.purpose = purpose;
	}



	public CountryPurposeDonor(String assoRcn, String assoName, String state, String district,String amount) {		
		this.assoRcn = assoRcn;
		this.assoName = assoName;
		this.district = district;
		this.state = state;
		this.amount = amount;
	}
	
	
	
	public String getAssoRcn() {
		return assoRcn;
	}
	public void setAssoRcn(String assoRcn) {
		this.assoRcn = assoRcn;
	}
	public String getAssoName() {
		return assoName;
	}
	public void setAssoName(String assoName) {
		this.assoName = assoName;
	}
	public String getDistrict() {
		return district;
	}
	public void setDistrict(String district) {
		this.district = district;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getBlockYear() {
		return blockYear;
	}

	public void setBlockYear(String blockYear) {
		this.blockYear = blockYear;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getDonor() {
		return donor;
	}

	public void setDonor(String donor) {
		this.donor = donor;
	}

	public String getPurpose() {
		return purpose;
	}

	public void setPurpose(String purpose) {
		this.purpose = purpose;
	}
}
