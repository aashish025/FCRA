package models.reports;

public class PurposeWiseReport {
	private String sino;
	private String stateName;
	private String countryName;
	private String purposeName;
	private String amount;
	private String noOfAssociation;
	private String blkYear;

	public PurposeWiseReport( String blkYear,  String purposeName, String amount, String noOfAssociation,String sino) {
		super();
		
		this.blkYear=blkYear;
		this.purposeName = purposeName;
		this.amount = amount;
		this.noOfAssociation = noOfAssociation;
		this.sino=sino;
	}

	public String getSino() {
		return sino;
	}

	public void setSino(String sino) {
		this.sino = sino;
	}

	public String getStateName() {
		return stateName;
	}

	public void setStateName(String stateName) {
		this.stateName = stateName;
	}

	public String getCountryName() {
		return countryName;
	}

	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}

	public String getPurposeName() {
		return purposeName;
	}

	public void setPurposeName(String purposeName) {
		this.purposeName = purposeName;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getNoOfAssociation() {
		return noOfAssociation;
	}

	public void setNoOfAssociation(String noOfAssociation) {
		this.noOfAssociation = noOfAssociation;
	}

	public String getBlkYear() {
		return blkYear;
	}

	public void setBlkYear(String blkYear) {
		this.blkYear = blkYear;
	}

}
