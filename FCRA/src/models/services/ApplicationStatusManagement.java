package models.services;

public class ApplicationStatusManagement {
	private String tempFileNo;
	private String sectionFileNo;
	private String applicationId;
	private String filestatusName;
	private String applicationName;
	private String serviceCode;
	private String serviceName;
	private String submissionDate;
	private String currentStatus;
	private String userName;
	private String userId;
	private String designation;
	/** default constructor */
	public ApplicationStatusManagement() {
	}
	public ApplicationStatusManagement(String tempFileNo, String sectionFileNo, String applicationId,  String applicationName,String serviceName,String serviceCode, String submissionDate,String currentStatus,String filestatusName)
	{
		this.tempFileNo = tempFileNo;
		this.sectionFileNo = sectionFileNo;
		this.applicationId = applicationId;
		
		this.applicationName = applicationName;
		this.serviceName = serviceName;
		this.serviceCode = serviceCode;
		
		this.submissionDate = submissionDate;
		this.currentStatus = currentStatus;
		this.filestatusName = filestatusName;
		
	}
	public ApplicationStatusManagement(String userId, String userName, String designation)
	{
		this.userId = userId;
		this.userName = userName;
		this.designation = designation;
		
		
	}
	
	
	
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getDesignation() {
		return designation;
	}
	public void setDesignation(String designation) {
		this.designation = designation;
	}
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
	public String getFilestatusName() {
		return filestatusName;
	}
	public void setFilestatusName(String filestatusName) {
		this.filestatusName = filestatusName;
	}
	public String getTempFileNo() {
		return tempFileNo;
	}
	public void setTempFileNo(String tempFileNo) {
		this.tempFileNo = tempFileNo;
	}
	public String getSectionFileNo() {
		return sectionFileNo;
	}
	public void setSectionFileNo(String sectionFileNo) {
		this.sectionFileNo = sectionFileNo;
	}
	public String getApplicationId() {
		return applicationId;
	}
	public void setApplicationId(String applicationId) {
		this.applicationId = applicationId;
	}
	
	public String getApplicationName() {
		return applicationName;
	}
	public void setApplicationName(String applicationName) {
		this.applicationName = applicationName;
	}
	public String getServiceCode() {
		return serviceCode;
	}
	public void setServiceCode(String serviceCode) {
		this.serviceCode = serviceCode;
	}
	public String getSubmissionDate() {
		return submissionDate;
	}
	public void setSubmissionDate(String submissionDate) {
		this.submissionDate = submissionDate;
	}
	public String getCurrentStatus() {
		return currentStatus;
	}
	public void setCurrentStatus(String currentStatus) {
		this.currentStatus = currentStatus;
	}

	
	
}
