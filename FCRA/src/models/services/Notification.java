package models.services;

public class Notification {
	/** class variables */
	private String notificationId;
	private String notificationTypeId;
	private String source_office;
	private String destination_office;
	private String fromUser;
	private String toUser;
	private String enteredBy;
	private String enteredOn;
	private String recordStatus;
	private String messageTitle;
	private String messageDetails;
	private Boolean docStatus;
	private String notificationStatus;
	private String newNotificationCount;
	private String rowId;
	
	/** default constructor */
	public Notification() {		
	}
	
	
	
	public Notification(String notificationId,String rowId){
		this.notificationId=notificationId;
		this.rowId=rowId;
	}
	
	public Notification(String notificationTypeId,String messageTitle,String messageDetails){
		this.notificationTypeId=notificationTypeId;
		this.messageTitle=messageTitle;
		this.messageDetails=messageDetails;		
	}
	
	public Notification(String notificationId,String notificationTypeId,String messageTitle, String messageDetails,String enteredOn,String enteredBy){
		this.notificationId=notificationId;
		this.notificationTypeId=notificationTypeId;
		this.messageTitle=messageTitle;
		this.messageDetails=messageDetails;
		this.enteredOn=enteredOn;
	}
	
	public Notification(String messageTitle, String messageDetails,String enteredOn,String notificationStatus,String notificationId) {		
		this.enteredOn = enteredOn;		
		this.messageTitle = messageTitle;
		this.messageDetails = messageDetails;
		this.notificationStatus=notificationStatus;	
		this.notificationId=notificationId;
	}
	

	public Notification(String notificationId, String notificationTypeId,
			String source_office, String destination_office, String fromUser,
			String toUser, String enteredBy, String enteredOn,
			String recordStatus, String messageTitle, String messageDetails,
			Boolean docStatus) {		
		this.notificationId = notificationId;
		this.notificationTypeId = notificationTypeId;
		this.source_office = source_office;
		this.destination_office = destination_office;
		this.fromUser = fromUser;
		this.toUser = toUser;
		this.enteredBy = enteredBy;
		this.enteredOn = enteredOn;
		this.recordStatus = recordStatus;
		this.messageTitle = messageTitle;
		this.messageDetails = messageDetails;
		this.docStatus = docStatus;
	}




	public String getNotificationId() {
		return notificationId;
	}

	public void setNotificationId(String notificationId) {
		this.notificationId = notificationId;
	}

	public String getNotificationTypeId() {
		return notificationTypeId;
	}

	public void setNotificationTypeId(String notificationTypeId) {
		this.notificationTypeId = notificationTypeId;
	}

	public String getSource_office() {
		return source_office;
	}

	public void setSource_office(String source_office) {
		this.source_office = source_office;
	}

	public String getDestination_office() {
		return destination_office;
	}

	public void setDestination_office(String destination_office) {
		this.destination_office = destination_office;
	}

	public String getFromUser() {
		return fromUser;
	}

	public void setFromUser(String fromUser) {
		this.fromUser = fromUser;
	}

	public String getToUser() {
		return toUser;
	}

	public void setToUser(String toUser) {
		this.toUser = toUser;
	}

	public String getEnteredBy() {
		return enteredBy;
	}

	public void setEnteredBy(String enteredBy) {
		this.enteredBy = enteredBy;
	}

	public String getEnteredOn() {
		return enteredOn;
	}

	public void setEnteredOn(String enteredOn) {
		this.enteredOn = enteredOn;
	}

	public String getRecordStatus() {
		return recordStatus;
	}

	public void setRecordStatus(String recordStatus) {
		this.recordStatus = recordStatus;
	}

	

	public Boolean getDocStatus() {
		return docStatus;
	}

	public void setDocStatus(Boolean docStatus) {
		this.docStatus = docStatus;
	}

	public String getMessageTitle() {
		return messageTitle;
	}

	public void setMessageTitle(String messageTitle) {
		this.messageTitle = messageTitle;
	}

	public String getMessageDetails() {
		return messageDetails;
	}

	public void setMessageDetails(String messageDetails) {
		this.messageDetails = messageDetails;
	}

	public String getNotificationStatus() {
		return notificationStatus;
	}

	public void setNotificationStatus(String notificationStatus) {
		this.notificationStatus = notificationStatus;
	}

	public String getNewNotificationCount() {
		return newNotificationCount;
	}

	public void setNewNotificationCount(String newNotificationCount) {
		this.newNotificationCount = newNotificationCount;
	}



	public String getRowId() {
		return rowId;
	}



	public void setRowId(String rowId) {
		this.rowId = rowId;
	}
	
}
