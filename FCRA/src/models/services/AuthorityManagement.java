package models.services;

public class AuthorityManagement {
private String applicationId;
private String serviceCode;
private String serviceDesc;
private String applicantName;
private String currentStatus;
private String sectionFileNo;
private String state;



public AuthorityManagement(String applicationId, String serviceCode,
		String serviceDesc, String currentStatus, String applicantName,String state) {
	super();
	this.applicationId = applicationId;
	this.serviceCode = serviceCode;
	this.serviceDesc = serviceDesc;
	this.currentStatus = currentStatus;
	this.applicantName = applicantName;
	this.state=state;
}


public AuthorityManagement(String applicationId, String sectionFileNo, String serviceDesc,String applicantName) {
	super();
	this.applicationId = applicationId;
	this.serviceDesc = serviceDesc;
	this.applicantName = applicantName;
	this.sectionFileNo = sectionFileNo;
}


public String getApplicationId() {
	return applicationId;
}
public void setApplicationId(String applicationId) {
	this.applicationId = applicationId;
}
public String getServiceCode() {
	return serviceCode;
}
public void setServiceCode(String serviceCode) {
	this.serviceCode = serviceCode;
}
public String getServiceDesc() {
	return serviceDesc;
}
public void setServiceDesc(String serviceDesc) {
	this.serviceDesc = serviceDesc;
}

public String getApplicantName() {
	return applicantName;
}
public void setApplicantName(String applicantName) {
	this.applicantName = applicantName;
}
public String getCurrentStatus() {
	return currentStatus;
}
public void setCurrentStatus(String currentStatus) {
	this.currentStatus = currentStatus;
}
public String getSectionFileNo() {
	return sectionFileNo;
}
public void setSectionFileNo(String sectionFileNo) {
	this.sectionFileNo = sectionFileNo;
}


public String getState() {
	return state;
}


public void setState(String state) {
	this.state = state;
}

}
