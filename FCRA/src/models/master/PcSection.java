package models.master;

import java.util.Date;

public class PcSection{
	// Fields
	private String sectionId;
	private String sectionName;
	private Short displayOrder;
	private String recordStatus;
	private String createdBy;
	private String createdIp;
	private String createdDate;
	private String rowIdentifier;
	private String lastModifiedIp;
	private Integer lastModifiedDate;
	private String lastModifiedBy;
    private String assignedServices;
    private String myOfficeCode;

	// Constructors

	/** default constructor */
	public PcSection() {
	}
	
	/** minimest constructor */
	public PcSection(String sectionId) {
		this.sectionId = sectionId;
	}

	public PcSection(String sectionId, String sectionName,String createdDate,String myofficeCode) {
		this.sectionId = sectionId;
		this.sectionName = sectionName;
		this.createdDate = createdDate;
		this.myOfficeCode=myofficeCode;
	}
		
	
	/** minimal constructor */
	public PcSection(String sectionId, String sectionName, String recordStatus, String createdBy,
			String createdIp, String createdDate) {
		this.sectionId = sectionId;
		this.sectionName = sectionName;
		this.recordStatus = recordStatus;
		this.createdBy = createdBy;
		this.createdIp = createdIp;
		this.createdDate = createdDate;
	}

	/** full constructor */
	public PcSection(String sectionId, String sectionName, Short displayOrder, String recordStatus,
			String createdBy, String createdIp, String createdDate) {
		this.sectionId = sectionId;
		this.sectionName = sectionName;
		this.displayOrder = displayOrder;
		this.recordStatus = recordStatus;
		this.createdBy = createdBy;
		this.createdIp = createdIp;
		this.createdDate = createdDate;
	}

	public String getSectionId() {
		return sectionId;
	}

	public void setSectionId(String sectionId) {
		this.sectionId = sectionId;
	}

	public String getSectionName() {
		return sectionName;
	}

	public void setSectionName(String sectionName) {
		this.sectionName = sectionName;
	}

	public Short getDisplayOrder() {
		return displayOrder;
	}

	public void setDisplayOrder(Short displayOrder) {
		this.displayOrder = displayOrder;
	}

	public String getRecordStatus() {
		return recordStatus;
	}

	public void setRecordStatus(String recordStatus) {
		this.recordStatus = recordStatus;
	}

	public String getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}

	public String getCreatedIp() {
		return createdIp;
	}

	public void setCreatedIp(String createdIp) {
		this.createdIp = createdIp;
	}

	public String getCreatedDate() {
		return createdDate;
	}

	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}

	public String getRowIdentifier() {
		return rowIdentifier;
	}

	public void setRowIdentifier(String rowIdentifier) {
		this.rowIdentifier = rowIdentifier;
	}

	public String getLastModifiedIp() {
		return lastModifiedIp;
	}

	public void setLastModifiedIp(String lastModifiedIp) {
		this.lastModifiedIp = lastModifiedIp;
	}

	public Integer getLastModifiedDate() {
		return lastModifiedDate;
	}

	public void setLastModifiedDate(Integer lastModifiedDate) {
		this.lastModifiedDate = lastModifiedDate;
	}

	public String getLastModifiedBy() {
		return lastModifiedBy;
	}

	public void setLastModifiedBy(String lastModifiedBy) {
		this.lastModifiedBy = lastModifiedBy;
	}

	public String getAssignedServices() {
		return assignedServices;
	}

	public void setAssignedServices(String assignedServices) {
		this.assignedServices = assignedServices;
	}

	public String getMyOfficeCode() {
		return myOfficeCode;
	}

	public void setMyOfficeCode(String myOfficeCode) {
		this.myOfficeCode = myOfficeCode;
	}


	
	
}