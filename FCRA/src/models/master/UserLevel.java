		this.userlevelid = userlavelId;
		this.userlevelName = userlavelName;
		this.displayOrder = displayOrder;
		this.recordStatus = recordStatus;
		this.createdBy = createdBy;
		this.createdIp = createdIp;
		this.createdDate = createdDate;
			
