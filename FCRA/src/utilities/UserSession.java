package utilities;

import java.sql.Connection;
import java.util.List;
import models.master.Office;
import models.master.ParentMenu;
import models.master.Role;

public class UserSession {
	private String userId;
	private String userName;
	private String userDesignation;
	private String userDesignationId;
	
	private String ip;
	private String mac;
	private String level;
	private Boolean gtt=false;
	private Boolean forcePasswordChange;
	private List<ParentMenu> parentMenuList;
	private Office office;
	private List<Role> roleList;
	private Connection connection;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getMac() {
		return mac;
	}
	public void setMac(String mac) {
		this.mac = mac;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public Boolean getForcePasswordChange() {
		return forcePasswordChange;
	}
	public void setForcePasswordChange(Boolean forcePasswordChange) {
		this.forcePasswordChange = forcePasswordChange;
	}
	public Office getOffice() {
		return office;
	}
	public void setOffice(Office office) {
		this.office = office;
	}
	public List<Role> getRoleList() {
		return roleList;
	}
	public void setRoleList(List<Role> roleList) {
		this.roleList = roleList;
	}
	public List<ParentMenu> getParentMenuList() {
		return parentMenuList;
	}
	public void setParentMenuList(List<ParentMenu> parentMenuList) {
		this.parentMenuList = parentMenuList;
	}
	public Connection getConnection() {
		return connection;
	}
	public void setConnection(Connection connection) {
		this.connection = connection;
	}
	public Boolean getGtt() {
		return gtt;
	}
	public void setGtt(Boolean gtt) {
		this.gtt = gtt;
	}
	public String getUserDesignation() {
		return userDesignation;
	}
	public void setUserDesignation(String userDesignation) {
		this.userDesignation = userDesignation;
	}
	public String getUserDesignationId() {
		return userDesignationId;
	}
	public void setUserDesignationId(String userDesignationId) {
		this.userDesignationId = userDesignationId;
	}
}
