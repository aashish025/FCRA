package utilities.lists;

public class List4 {
	private String p1;
	private String p2;
	private String p3;
	private String p4;
	
	public List4(String p1, String p2, String p3, String p4) {
		this.p1 = p1;
		this.p2 = p2;
		this.p3 = p3;
		this.p4 = p4;
	}
	
	public List4(StringBuffer p1, StringBuffer p2, StringBuffer p3, StringBuffer p4) {
		this.p1 = p1.toString();
		this.p2 = p2.toString();
		this.p3 = p3.toString();
		this.p4 = p4.toString();
	}
	public String getP1() {
		return p1;
	}
	public void setP1(String p1) {
		this.p1 = p1;
	}
	public String getP2() {
		return p2;
	}
	public void setP2(String p2) {
		this.p2 = p2;
	}
	public String getP3() {
		return p3;
	}
	public void setP3(String p3) {
		this.p3 = p3;
	}
	public String getP4() {
		return p4;
	}
	public void setP4(String p4) {
		this.p4 = p4;
	}
}
